import turtle
def draw(c):
    t.begin_fill()
    t.fillcolor(c)
    for i in range(2):
        t.forward(300)
        t.right(90)
        t.forward(50)
        t.right(90)
    t.end_fill()
    
t=turtle.Turtle()
s=turtle.Screen()
s.bgcolor('ivory')
s.title('INDIAN FLAG')

t.pensize(4)
t.up()
t.goto(0,-300)
t.down()
t.goto(0,200)
draw('#FF9933')

t.goto(0,150)
t.forward(150)
t.color('#000088')
t.circle(-25)
t.setheading(270)
t.forward(25)
t.setheading(0)
for i in range(24):
    t.forward(23)
    t.bk(23)
    t.left(15)
t.setheading(90)
t.forward(25)
t.setheading(0)
t.color('black')
t.forward(150)
t.right(90)
t.forward(50)
t.right(90)
t.forward(300)
t.right(90)
t.forward(50)
t.right(90)
t.goto(0,100)
draw('#128807')
t.right(90)
